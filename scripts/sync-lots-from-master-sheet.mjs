#!/usr/bin/env node
/**
 * Sync lots from the master Google Sheet via the Apps Script API.
 *
 * Runs at build time (npm `prebuild` script) so the static site always
 * ships with the latest sheet state. Generates src/data/_generatedMasterLots.ts
 * which is imported by lots.ts.
 *
 * The master sheet lives at sheet ID 1ES3JjgZaqj_uEci3DdExDF-eC8eumDz0wrvb8avEB9g
 * (see ~/Odyssey-Code/upgrades-and-options/Code.js for the data layout).
 * The Apps Script web app is deployed as "Anyone, even anonymous" so this
 * script needs no auth.
 *
 * To run manually: `node scripts/sync-lots-from-master-sheet.mjs`
 */

import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const OUT_FILE = join(ROOT, 'src/data/_generatedMasterLots.ts');

const API_URL = 'https://script.google.com/macros/s/AKfycbyCBOgU6SyWUAgerb-_2iT9gOJmwgdR4-vRmahXMTIjgfchH7tzCJ1ufNLkVkovjwYnYQ/exec?api=lot-inventory';

// API status → website LotStatus. The sheet uses 'spec-in-progress' which
// the website renders with a distinct yellow badge.
function mapStatus(apiStatus) {
  switch (apiStatus) {
    case 'available':         return 'available';
    case 'spec-in-progress':  return 'spec-in-progress';
    case 'reserved':          return 'reserved';
    case 'under-contract':    return 'under-contract';
    case 'sold':              return 'sold';
    default:                  return 'available';
  }
}

// Map "Granite Creek" → "granite-creek" etc. Mirrors what the Apps Script
// does, but kept here as a safety net.
function slugify(name) {
  return String(name || '').toLowerCase().trim().replace(/[^\w\s-]/g, '').replace(/\s+/g, '-');
}

function lotDisplayLabel(lot) {
  // "Lot 8, Block 7" — matches the existing UI convention
  if (lot.lot != null && lot.block != null) {
    return `Lot ${lot.lot}, Block ${lot.block}`;
  }
  return lot.lotNumber || '—';
}

async function main() {
  console.log(`[sync-lots] Fetching ${API_URL}…`);
  const res = await fetch(API_URL);
  if (!res.ok) {
    throw new Error(`API returned ${res.status}: ${await res.text()}`);
  }
  const payload = await res.json();
  if (!Array.isArray(payload.lots)) {
    throw new Error(`Unexpected payload shape: ${JSON.stringify(payload).slice(0, 200)}`);
  }

  console.log(`[sync-lots] Got ${payload.lots.length} lots from sheet (synced at ${payload.syncedAt}).`);

  const lots = payload.lots.map(l => {
    const slug = l.communitySlug || slugify(l.subdivision);
    return {
      id: l.id || `${slug}-${l.lotNumber?.replace(/\s+/g, '')}`,
      communitySlug: slug,
      lotNumber: lotDisplayLabel(l),
      address: l.address || undefined,
      acres: typeof l.acres === 'number' ? l.acres : undefined,
      price: typeof l.price === 'number' ? l.price : undefined,
      status: mapStatus(l.status),
      notes: l.notes || undefined,
    };
  });

  const file = `// AUTO-GENERATED by scripts/sync-lots-from-master-sheet.mjs
// DO NOT EDIT BY HAND — your changes will be overwritten on next build.
// To update lot data: edit the master Google Sheet
// (1ES3JjgZaqj_uEci3DdExDF-eC8eumDz0wrvb8avEB9g) and re-run sync.
//
// Synced: ${payload.syncedAt}
// Source: Apps Script ?api=lot-inventory

import type { Lot } from './lots';

export const generatedMasterLots: Lot[] = ${JSON.stringify(lots, null, 2)};
`;

  writeFileSync(OUT_FILE, file, 'utf8');
  console.log(`[sync-lots] Wrote ${lots.length} lots to ${OUT_FILE.replace(ROOT + '/', '')}`);

  // Print status summary
  const byStatus = {};
  for (const l of lots) byStatus[l.status] = (byStatus[l.status] || 0) + 1;
  console.log('[sync-lots] Status breakdown:');
  for (const [status, count] of Object.entries(byStatus)) {
    console.log(`  ${status}: ${count}`);
  }
}

main().catch(err => {
  console.error('[sync-lots] FAILED:', err.message);
  // Exit 0 so a transient API failure doesn't block a deploy — fall back
  // to whatever's already in _generatedMasterLots.ts.
  console.error('[sync-lots] Continuing with existing _generatedMasterLots.ts (if any).');
  process.exit(0);
});
